public class Mobiles {
    int cost;
    String ram;
    String color;

    public int getCost(){
        return cost;
    }
    public void setCost(int cost){
        this.cost = cost;
    }
    public String getRam(){
        return ram;
    }
    public void setRam(String ram){
        this.ram = ram;
    }
    public String getColor(){
        return color;
    }
    public void setColor(String color){
        this.color = color;
    }
}
